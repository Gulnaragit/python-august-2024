foods = ["burger", "pizza", "sushi", "taco"] #list, can be modified by appending or extending. Uses index
print(foods)
print(foods[0])

drinks = ["coke", "pepsi", "juice", "coffee"]
print(drinks)

foods.append("salad") # we can append only 1 value
print(foods)

foods.extend(["burrito", "cheeseburger", "fish"]) # multiple values can be appended using list []
print(foods)
